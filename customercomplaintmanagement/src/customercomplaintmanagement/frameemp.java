            ecode=(int)jTable2.getValueAt(idx, 0);
            txtname.setText(jTable2.getValueAt(idx, 1).toString());
            txtphnno.setText(jTable2.getValueAt(idx, 2).toString());
            cmbloc.setSelectedItem(jTable2.getValueAt(idx, 3));
