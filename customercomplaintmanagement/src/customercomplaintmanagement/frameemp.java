    private void loaddata(){
        DefaultTableModel mod=(DefaultTableModel)jTable2.getModel();
        clsemp obj=new clsemp();
        try {
            ArrayList<clsempprp> k=obj.disemp();
            mod.setRowCount(0);
            for(clsempprp p:k){
                Object[] rowdata=new Object[5];
                rowdata[0]=p.getEmpcode();
                rowdata[1]=p.getEmpname();
                rowdata[2]=p.getEmpphnno();
                rowdata[3]=p.getEmploc();
                ImageIcon img=new ImageIcon(getClass().getResource("/emppics/"+p.getEmppic()));
                rowdata[4]=img;
                mod.addRow(rowdata);
            }
            jTable1.setModel(mod);
        } catch (IOException ex) {
            Logger.getLogger(frameemp.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frameemp.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frameemp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void saveimage(){
        try {
            String s=txtpic.getText();
            if(s.equals("")==false)
            {
            Path source=Paths.get(txtpic.getText());
            
            Path targetdir=Paths.get("E:\\javafiles\\customercomplaintmanagement\\build\\classes\\emppics");
            Files.createDirectories(targetdir);
            
            s=s.substring(s.lastIndexOf("\\")+1);
            Path target=targetdir.resolve(s);
            Files.copy(source, target,StandardCopyOption.REPLACE_EXISTING);
            }
        } catch (IOException ex) {
            Logger.getLogger(frameemp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
